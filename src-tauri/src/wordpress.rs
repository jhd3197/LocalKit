//! WordPress management via wp-cli.
//!
//! The stock `wordpress` image does NOT bundle wp-cli, so each site compose
//! file includes a profile-gated `wpcli` service (the official
//! `wordpress:cli` image) and we run commands with
//! `docker compose run --rm -T wpcli wp <args...>`.

use serde::{Deserialize, Serialize};
use std::path::{Path, PathBuf};
use tauri::AppHandle;

use crate::docker;
use crate::site::{self, Site};

#[derive(Debug, Clone, Serialize)]
pub struct PluginInfo {
    pub name: String,
    pub status: String,
    pub version: String,
}

#[derive(Debug, Clone, Serialize)]
pub struct WpInfo {
    pub core_version: String,
    pub plugins: Vec<PluginInfo>,
}

#[derive(Deserialize)]
struct RawPlugin {
    name: String,
    status: String,
    #[serde(default)]
    version: String,
}

/// A WordPress user, for the WP Admin login picker.
#[derive(Debug, Clone, Serialize)]
pub struct WpUser {
    pub id: u64,
    pub login: String,
    pub name: String,
    pub roles: String,
}

#[derive(Deserialize)]
struct RawUser {
    #[serde(rename = "ID")]
    id: u64,
    user_login: String,
    #[serde(default)]
    display_name: String,
    #[serde(default)]
    roles: String,
}

